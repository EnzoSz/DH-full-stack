//tipo de dato number

let edad = 27;
// number con decimal
let peso = 77.5;
// con el operador 'typeof' podemos ver el tipo de dato que es la variable
console.log(typeof edad, peso);

//tipo de dato STRING
//el valor del string puede ir con comillas dobles o simples

let nombre = 'Enzo';
console.log(nombre);

//tipo de dato boolean 

let verdadero = true;
let falso = false;
console.log(verdadero, falso);

//tipo de dato NaN

let notANumber = NaN;
console.log(notANumber);

//tipo de dato null

let nulo = null;
console.log(nulo);

// tipo de dato undefined

let sinValor;
console.log(sinValor);

// tipo de dato objeto literal

let persona = {
    nombre: 'Enzo',
    edad: 27
}
console.log(persona);

// tipo de dato array

let paises = ['Argentina', 'Colombia', 'Venezuela'];
console.log(paises);

